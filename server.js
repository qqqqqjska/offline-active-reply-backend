async function createOfflineMessage(row, now, options = {}) {
    const referenceTime = Number(options.referenceTime || row.time || now);
    const triggerMode = String(options.triggerMode || 'after-message');
    const consumedSnapshotId = options.consumedSnapshotId !== undefined
        ? options.consumedSnapshotId
        : (row.message_id || null);
    const messageStateId = String(options.messageStateId || row.message_id || consumedSnapshotId || `timer-${referenceTime}`);
    const minutesPassed = Math.max(1, Math.floor((now - referenceTime) / 60000));
    const rowForGeneration = {
        ...row,
        time: referenceTime
    };
    const generated = await generateAiReplyContent(rowForGeneration, minutesPassed, triggerMode);
    const prompt = buildActiveReplyInstruction(rowForGeneration, minutesPassed, triggerMode);
    const sourceContentForSplit = typeof generated.rawContent === 'string' && generated.rawContent.trim()
        ? generated.rawContent
        : generated.content;
    const visibleMessages = extractVisibleMessagesFromMixedResponse(sourceContentForSplit)
        .map((item) => ({
            type: item.type || 'text',
            content: String(item.content || '').trim(),
            description: typeof item.description === 'string' && item.description.trim() ? item.description.trim() : null
        }))
        .filter((item) => item.content);
    const normalizedMessages = visibleMessages.length > 0
        ? visibleMessages
        : [{ type: 'text', content: fallbackMessage(row.name || '对方', rowForGeneration, triggerMode) }];

    const getPreviewText = (messageItem) => {
        if (!messageItem || typeof messageItem !== 'object') return '';
        if (messageItem.type === 'sticker') {
            return '[动画表情]';
        }
        if (messageItem.type === 'image' || messageItem.type === 'virtual_image') {
            return isLikelyAnimatedPreviewUrl(messageItem.content) ? '[动画表情]' : '[图片]';
        }
        if (messageItem.type === 'voice') {
            return '[语音]';
        }
        if (isLikelyAnimatedPreviewUrl(messageItem.content) || isLikelyAnimatedPreviewUrl(messageItem.description)) {
            return '[动画表情]';
        }
        if (isLikelyImagePreviewUrl(messageItem.content)) {
            return '[图片]';
        }
        if (messageItem.type === 'sticker') {
            return `[表情包] ${messageItem.description || messageItem.content || ''}`.trim();
        }
        if (messageItem.type === 'image' || messageItem.type === 'virtual_image') {
            return '[图片]';
        }
        if (messageItem.type === 'voice') {
            return '[语音]';
        }
        return String(messageItem.content || '').trim();
    };

    let insertedCount = 0;
    let firstMessageId = null;
    let firstContent = getPreviewText(normalizedMessages[0]);
    const insertedMessages = [];

    normalizedMessages.forEach((messageItem, index) => {
        const messageTime = now + index;
        const messageId = `offline-${row.user_id}-${row.contact_id}-${messageStateId}-${messageTime}-${index}`;
        const insertResult = insertMessageStmt.run({
            id: messageId,
            user_id: row.user_id,
            contact_id: String(row.contact_id),
            role: 'assistant',
            content: messageItem.content,
            type: messageItem.type || 'text',
            description: messageItem.description || prompt,
            time: messageTime,
            read: 0,
            source: 'offline-backend'
        });
        if (insertResult.changes) {
            insertedCount += 1;
            insertedMessages.push({
                id: messageId,
                contactId: String(row.contact_id),
                role: 'assistant',
                content: messageItem.content,
                preview: getPreviewText(messageItem),
                type: messageItem.type || 'text',
                description: messageItem.description || prompt,
                time: messageTime,
                read: false,
                source: 'offline-backend'
            });
            if (!firstMessageId) {
                firstMessageId = messageId;
            }
        }
    });

    if (!insertedCount || !firstMessageId) {
        return null;
    }

    normalizedMessages.forEach((messageItem, index) => {
        syncRowRestStateFromAssistantMessage(row, messageItem, now + index);
    });

    updateTriggerStateStmt.run(firstMessageId, now, consumedSnapshotId, now, row.user_id, String(row.contact_id));

    const push = {
        enabled: PUSH_ENABLED,
        delivered: 0,
        removed: 0,
        attempted: 0
    };
    for (const insertedMessage of insertedMessages) {
        const notificationTitle = row.name ? String(row.name).trim() : '新消息';
        const notificationBody = insertedMessage.preview || insertedMessage.content;
        const avatarUrl = resolvePushAvatarUrl(row);
        const pushResult = await sendPushToUser(row.user_id, {
            title: notificationTitle,
            body: notificationBody,
            icon: avatarUrl,
            avatarUrl,
            contactAvatar: avatarUrl,
            badge: avatarUrl,
            tag: `contact-${row.contact_id}-${insertedMessage.id}`,
            contactId: String(row.contact_id),
            url: `./?contactId=${encodeURIComponent(String(row.contact_id))}&openChat=1`,
            data: {
                contactId: String(row.contact_id),
                contactName: notificationTitle,
                icon: avatarUrl,
                avatarUrl,
                contactAvatar: avatarUrl,
                badge: avatarUrl,
                messageId: insertedMessage.id,
                message: {
                    id: insertedMessage.id,
                    contactId: insertedMessage.contactId,
                    role: insertedMessage.role,
                    content: insertedMessage.content,
                    type: insertedMessage.type,
                    description: insertedMessage.description,
                    time: insertedMessage.time,
                    read: insertedMessage.read,
                    source: insertedMessage.source
                },
                url: `./?contactId=${encodeURIComponent(String(row.contact_id))}&openChat=1`
            }
        });
        push.attempted += 1;
        push.delivered += Number(pushResult && pushResult.delivered || 0);
        push.removed += Number(pushResult && pushResult.removed || 0);
    }

    return {
        id: firstMessageId,
        contactId: String(row.contact_id),
        content: firstContent,
        time: now,
        push,
        messageCount: insertedCount,
        usedFallback: generated.usedFallback,
        debug: generated.debug,
        triggerMode
    };
}

function resolveActiveReplyTrigger(row, now) {
    const restState = getRestScheduleState(row, now);
    if (restState.inRestWindow && !isRestAwakeOverrideActive(row, now, restState)) {
        return null;
    }
    const activeReplyStartTime = Number(row.active_reply_start_time || 0);
    if (activeReplyStartTime > now) {
        return null;
    }

    const requiredMs = Math.max(1, Number(row.active_reply_interval_sec || 60)) * 1000;
    const snapshotId = row.message_id ? String(row.message_id) : '';
    const snapshotTime = Number(row.time || 0);
    const lastSeenMessageId = row.last_seen_message_id ? String(row.last_seen_message_id) : '';
    const lastTriggeredAt = Number(row.last_triggered_at || 0);
    const hasFreshSnapshot = Boolean(snapshotId)
        && snapshotTime > 0
        && snapshotTime > activeReplyStartTime
        && snapshotId !== lastSeenMessageId;

    if (hasFreshSnapshot) {
        if ((now - snapshotTime) < requiredMs) {
            return null;
        }
        return {
            triggerMode: 'after-message',
            referenceTime: snapshotTime,
            consumedSnapshotId: snapshotId,
            messageStateId: snapshotId
        };
    }

    const baselineTime = Math.max(lastTriggeredAt, activeReplyStartTime);
    if (!baselineTime) {
        return null;
    }
    if ((now - baselineTime) < requiredMs) {
        return null;
    }

    return {
        triggerMode: lastTriggeredAt > 0 ? 'scheduled-followup' : 'scheduled-opening',
        referenceTime: baselineTime,
        consumedSnapshotId: snapshotId || lastSeenMessageId || null,
        messageStateId: snapshotId || lastSeenMessageId || `timer-${baselineTime}`
    };
}

async function runActiveReplyCheck() {
    if (activeReplyCheckPromise) {
        return activeReplyCheckPromise;
    }

    activeReplyCheckPromise = (async () => {
        const rows = db.prepare(`
        SELECT c.user_id, c.contact_id, c.name, c.avatar_url, c.persona_prompt, c.context_limit, c.active_reply_enabled, c.active_reply_interval_sec, c.active_reply_start_time,
               c.rest_schedule_enabled, c.rest_schedule_start_minute, c.rest_schedule_end_minute, c.rest_awake_override, c.rest_awake_window_key,
               c.last_triggered_msg_id, c.last_triggered_at, c.last_seen_message_id,
               s.message_id, s.role, s.content, s.type, s.time
        FROM contacts c
        LEFT JOIN message_snapshots s ON s.user_id = c.user_id AND s.contact_id = c.contact_id
        WHERE c.active_reply_enabled = 1
    `).all();

        const now = Date.now();
        let triggered = 0;

        for (const row of rows) {
            refreshStoredRestState(row, now);
            const trigger = resolveActiveReplyTrigger(row, now);
            if (!trigger) continue;

            const created = await createOfflineMessage(row, now, trigger);
            if (created) {
                triggered += 1;
            }
        }

        return triggered;
    })();

    try {
        return await activeReplyCheckPromise;
    } finally {
        activeReplyCheckPromise = null;
    }
}
