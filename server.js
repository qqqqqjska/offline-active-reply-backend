function extractJsonBlocksFromText(text) {
    const source = String(text || '');
    const found = [];
    const stack = [];
    let inString = false;
    let escape = false;
    let blockStart = -1;

    for (let i = 0; i < source.length; i++) {
        const char = source[i];
        if (inString) {
            if (char === '\\' && !escape) {
                escape = true;
            } else if (char === '"' && !escape) {
                inString = false;
            } else {
                escape = false;
            }
            continue;
        }

        if (char === '"') {
            inString = true;
            escape = false;
            continue;
        }

        if (char === '{' || char === '[') {
            if (stack.length === 0) blockStart = i;
            stack.push(char);
            continue;
        }

        if (char === '}' || char === ']') {
            if (!stack.length) continue;
            const expected = char === '}' ? '{' : '[';
            if (stack[stack.length - 1] !== expected) {
                stack.length = 0;
                blockStart = -1;
                continue;
            }
            stack.pop();
            if (stack.length === 0 && blockStart >= 0) {
                found.push(source.slice(blockStart, i + 1));
                blockStart = -1;
            }
        }
    }

    return found;
}

function parseVisibleItemsFromMixedResponse(rawText) {
    const source = String(rawText || '').trim();
    if (!source) return null;

    const candidates = [];
    const seen = new Set();
    const pushCandidate = (value) => {
        const candidate = String(value || '').trim();
        if (!candidate || seen.has(candidate)) return;
        seen.add(candidate);
        candidates.push(candidate);
    };

    pushCandidate(source);

    const fencedJsonRegex = /```(?:json)?\s*([\s\S]*?)```/gi;
    let fencedMatch;
    while ((fencedMatch = fencedJsonRegex.exec(source)) !== null) {
        pushCandidate(fencedMatch[1]);
    }

    extractJsonBlocksFromText(source).forEach(pushCandidate);

    for (const candidate of candidates) {
        try {
            const parsed = JSON.parse(candidate);
            const items = Array.isArray(parsed) ? parsed : [parsed];
            if (items.some((item) => item && typeof item === 'object')) {
                return items;
            }
        } catch (err) {}
    }

    return null;
}

function extractVisibleTextFromMixedResponse(rawText) {
    const text = String(rawText || '').trim();
    if (!text) return '';

    const items = parseVisibleItemsFromMixedResponse(text);
    if (items) {
        const visibleTexts = [];
        items.forEach((item) => {
            if (!item || typeof item !== 'object') return;
            if (item.type === 'text_message' && typeof item.content === 'string' && item.content.trim()) {
                visibleTexts.push(item.content.trim());
            }
            if (item.type === 'quote_reply' && typeof item.reply_content === 'string' && item.reply_content.trim()) {
                visibleTexts.push(item.reply_content.trim());
            }
            if (item.type === 'sticker_message' && typeof item.sticker === 'string' && item.sticker.trim()) {
                visibleTexts.push(`[琛ㄦ儏鍖匽 ${item.sticker.trim()}`);
            }
            if (item.type === 'image' && typeof item.content === 'string' && item.content.trim()) {
                visibleTexts.push(item.content.trim());
            }
            if (item.type === 'voice' && typeof item.content === 'string' && item.content.trim()) {
                visibleTexts.push(item.content.trim());
            }
        });
        if (visibleTexts.length > 0) {
            return visibleTexts.join('\n');
        }
    }

    return text;
}

function extractVisibleMessagesFromMixedResponse(rawText, options = {}) {
    const text = String(rawText || '').trim();
    if (!text) return [];

    const splitPlainTextMessages = (value) => {
        const normalized = String(value || '')
            .replace(/\r\n?/g, '\n')
            .replace(/^```(?:json)?\s*/i, '')
            .replace(/\s*```$/i, '')
            .trim();
        if (!normalized) return [];
        const lines = normalized.split('\n').map((line) => line.trim()).filter(Boolean);
        if (lines.length <= 1) {
            return normalized ? [{ type: 'text', content: normalized }] : [];
        }
        return lines.map((line) => ({ type: 'text', content: line }));
    };

    const items = parseVisibleItemsFromMixedResponse(text);
    if (items) {
        const visibleMessages = [];
        items.forEach((item) => {
            if (!item || typeof item !== 'object') return;
            if (item.type === 'text_message' && typeof item.content === 'string' && item.content.trim()) {
                visibleMessages.push(...splitPlainTextMessages(item.content));
                return;
            }
            if (item.type === 'quote_reply' && typeof item.reply_content === 'string' && item.reply_content.trim()) {
                visibleMessages.push(...splitPlainTextMessages(item.reply_content));
                return;
            }
            if (item.type === 'sticker_message' && typeof item.sticker === 'string' && item.sticker.trim()) {
                const stickerName = item.sticker.trim();
                visibleMessages.push({ type: 'sticker', content: stickerName, description: stickerName });
                return;
            }
            if (item.type === 'image' && typeof item.content === 'string' && item.content.trim()) {
                const imageContent = item.content.trim();
                const imageDescription = typeof item.prompt === 'string' && item.prompt.trim()
                    ? item.prompt.trim()
                    : (typeof item.description === 'string' && item.description.trim() ? item.description.trim() : imageContent);
                if (isLikelyImagePreviewUrl(imageContent)) {
                    visibleMessages.push({ type: 'image', content: imageContent, description: imageDescription });
                } else {
                    const virtualImageDescription = imageDescription && imageDescription !== imageContent
                        ? `[图片] ${imageDescription}`
                        : '[图片]';
                    visibleMessages.push({
                        type: 'virtual_image',
                        content: OFFLINE_VIRTUAL_IMAGE_URL,
                        description: String(virtualImageDescription || '').replace(/^\[[^\]]+\]\s*/, '') || '图片描述'
                    });
                }
                return;
            }
            if (item.type === 'voice' && typeof item.content === 'string' && item.content.trim()) {
                visibleMessages.push({ type: 'voice', content: item.content.trim() });
            }
        });
        return visibleMessages;
    }

    return splitPlainTextMessages(text);
}

function extractTextFromAiResponsePart(part) {
    if (part === null || part === undefined) return '';
    if (typeof part === 'string') return part;

    if (Array.isArray(part)) {
        return part.map((item) => extractTextFromAiResponsePart(item)).filter(Boolean).join('\n');
    }

    if (typeof part !== 'object') return '';

    if (typeof part.text === 'string') return part.text;
    if (part.text && typeof part.text.value === 'string') return part.text.value;
    if (typeof part.value === 'string') return part.value;
    if (typeof part.content === 'string') return part.content;
    if (typeof part.output_text === 'string') return part.output_text;

    if (Array.isArray(part.content)) {
        return part.content.map((item) => extractTextFromAiResponsePart(item)).filter(Boolean).join('\n');
    }

    return '';
}

function extractReplyContentFromAiResponse(data) {
    const choice = data && Array.isArray(data.choices) ? data.choices[0] : null;
    const message = choice && choice.message ? choice.message : null;

    const candidates = [
        { source: 'choices[0].message.content', value: message ? message.content : null },
        { source: 'choices[0].text', value: choice ? choice.text : null },
        { source: 'choices[0].delta.content', value: choice && choice.delta ? choice.delta.content : null },
        { source: 'output_text', value: data ? data.output_text : null },
        { source: 'output[0].content', value: data && Array.isArray(data.output) && data.output[0] ? data.output[0].content : null }
    ];

    for (const candidate of candidates) {
        const text = extractTextFromAiResponsePart(candidate.value).trim();
        if (text) {
            return { content: text, source: candidate.source };
        }
    }

    return { content: '', source: null };
}

