function resolvePushAvatarUrl(row) {
    const candidate = String((row && (row.avatar_url || row.avatarUrl)) || '').trim();
    if (/^https?:\/\//i.test(candidate)) return candidate;
    if (/^data:image\//i.test(candidate) && candidate.length <= 1800) return candidate;
    const seed = encodeURIComponent(String((row && (row.name || row.contact_id)) || 'contact'));
    return `https://api.dicebear.com/7.x/avataaars/svg?seed=${seed}`;
}

function isLikelyImagePreviewUrl(value) {
    const text = String(value || '').trim();
    if (!text || !/^https?:\/\//i.test(text)) return false;
    if (/\.(?:png|jpe?g|gif|webp|bmp|svg|avif)(?:[?#].*)?$/i.test(text)) return true;
    return /(postimg\.cc|postimg\.org|placehold\.co|imgur\.com|image\.bdstatic\.com|qpic\.cn|alicdn\.com)/i.test(text);
}

function isLikelyAnimatedPreviewUrl(value) {
    const text = String(value || '').trim();
    if (!text) return false;
    return /\.(?:gif)(?:[?#].*)?$/i.test(text) || /(?:[?&](?:format|type)=gif\b)/i.test(text);
}

function extractCronSecretFromRequest(req) {
    const headerSecret = String(req.get('x-cron-secret') || req.get('x-offline-cron-secret') || '').trim();
    if (headerSecret) return headerSecret;

    const authHeader = String(req.get('authorization') || '').trim();
    const bearerMatch = authHeader.match(/^Bearer\s+(.+)$/i);
    if (bearerMatch && bearerMatch[1]) {
        return String(bearerMatch[1]).trim();
    }

    if (req.query && req.query.secret !== undefined) {
        return String(req.query.secret || '').trim();
    }

    if (req.body && typeof req.body === 'object') {
        if (req.body.secret !== undefined) return String(req.body.secret || '').trim();
        if (req.body.cronSecret !== undefined) return String(req.body.cronSecret || '').trim();
    }

    return '';
}

function ensureCronAuthorized(req, res) {
    const incomingSecret = extractCronSecretFromRequest(req);
    if (!incomingSecret || incomingSecret !== CRON_SECRET) {
        res.status(401).json({ ok: false, error: 'Invalid cron secret' });
        return false;
    }

    return true;
}

